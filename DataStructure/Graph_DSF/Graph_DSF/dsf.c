#include<stdio.h>
#include<memory.h>
#include<stdlib.h>
#define MAX_VERTEX 10
#define FALSE 0
#define TRUE 1

typedef struct graphNode {
	int vertex;
	struct graphNode* link;
}graphNode;

typedef struct graphType {
	int n; // 그래프의 마지막 정점 번호?
	graphNode * adjList_H[MAX_VERTEX]; // 포인터 배열 (인접리스트 헤드 포인터 배열) 
	int visited[MAX_VERTEX]; // 방문 정점 체크 배열
}graphType;

typedef int element;

typedef struct stackNode { // 마지막 방문한 정점 저장할 stack
	int data;
	struct stackNode *link;
}stackNode;

stackNode * top;

int isEmpty() {
	if (top == NULL) return 1;
	else return 0;
}

void push(int item) {
	stackNode * temp = (stackNode *)malloc(sizeof(stackNode));
	temp->data = item;
	temp->link = top;
	top = temp;
}

int pop() {
	int item;
	stackNode * temp = top;

	if (isEmpty()) {
		printf("\n\n Stack is Empty! \n");
		return 0;
	}
	else {
		item = temp->data;
		top = temp->link;
		free(temp);
		return item;
	}
} // 스택 >>

void createGraph(graphType *g) {
	int v;
	g->n = 0; 
	for (v = 0; v < MAX_VERTEX; v++) {
		g->visited[v] = FALSE;// 모든 노드의 방문 false
		g->adjList_H[v] = NULL; // 모든 헤드포인터를 null포인터로 지정
	}
}

void insertVertex(graphType *g, int v) {
	if (((g->n) + 1) > MAX_VERTEX) {
		printf("Over the vertex num");
		return;
	}
	g->n++;
}

void insertEdge(graphType *g, int u, int v) {
	graphNode * node;
	if (u >= g->n || v >= g->n) {
		printf("not existed vertex in graph");
		return;
	}
	node = (graphNode *)malloc(sizeof(graphNode)); // 메모리 동적할당
	node->vertex = v; 
	node->link = g->adjList_H[u];
	g->adjList_H[u] = node;
}

void printf_adjList(graphType *g) {
	int i;
	graphNode* p;
	for (i = 0; i < g->n; i++) {
		printf("\n\t\t정점 %c의 인접 리스트", i + 65);
		p = g->adjList_H[i];
		while (p){
			printf(" -> %c", p->vertex + 65);
			p = p->link;
		}
	}
}


// 그래프 g에서 정점 v에 대한 깊이 우선 탐색 연산
void DFS_adjList(graphType *g, int v) {
	graphNode * w;
	top = NULL;
	push(v);
	g->visited[v] = TRUE;
	printf(" %c", v + 65);

	while (!isEmpty()) {
		v = pop();
		w = g->adjList_H[v];
		while (w) {
			if (!g->visited[w->vertex]) {
				if (isEmpty()) push(v);
				push(w->vertex);
				g->visited[w->vertex] = TRUE;
				printf(" %c", w->vertex + 65);
				v = w->vertex;
				w = g->adjList_H[v];
			}
			else w = w->link;
		}
	}
}

